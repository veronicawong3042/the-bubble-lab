function register_category_menu() {
    register_nav_menu(
        'category-menu', 
        __('Category Menu') 
    );
}
add_action('init', 'register_category_menu');

function display_category_menu() {
    wp_nav_menu(
        array(
            'theme_location' => 'category-menu',
            'menu_class' => 'category-menu-class',
        )
    );
}




